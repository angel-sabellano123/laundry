         void displayProfile() {
        try {
            config con = new config();
            java.sql.Connection conn = con.connectDB();

            // Only get staff info
            String sql = "SELECT full_name, username, contact, role FROM laundry WHERE username = ? AND role='staff'";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, loggedInUsername);
            java.sql.ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                full_name.setText(rs.getString("full_name"));
                username.setText(rs.getString("username"));
                role.setText(rs.getString("role"));
            } else {
                JOptionPane.showMessageDialog(this, "Staff not found!");
            }


            rs.close();
            pst.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        }
    }

